namespace Leevel\Option\Proxy;

use Leevel\Di\Container;

/**
 * 代理 option.
 *
 * @author Xiangmin Liu <635750556@qq.com>
 *
 * @since 2017.06.10
 *
 * @version 1.0
 */
class Option
{
    /**
     * call.
     *
     * @param string $method
     * @param array  $args
     *
     * @return mixed
     */
    public static function __callStatic(string $method, array $args)
    {
        return Container::singletons()
            ->make('option')
            ->{$method}(...$args);
    }
}
